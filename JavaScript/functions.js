function squareRoot(num) {
  return Math.sqrt(num);
}

function cubicRoot(num) {
  return Math.cbrt(num);
}

let numero = 1;

console.log(squareRoot(numero));
console.log(cubicRoot(numero));

function circuference(num) {
  return Math.PI * num * 2;
}

console.log(circuference(numero));

